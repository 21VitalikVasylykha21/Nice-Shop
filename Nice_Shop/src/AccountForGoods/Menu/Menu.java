    private void PayKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_PayKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            int index = JTableCounters.getSelectedRow();
            TableModel model = JTableCounters.getModel();
            p = Double.parseDouble(model.getValueAt(index, 3).toString()) * (int) SpnrQualyte.getValue();
            try {
                pay = (double) Double.parseDouble(Pay.getText());
                if (pay < p) {
                    LblReshta.setText("Недостатньо грошей");
                    BtnPie.setEnabled(false);
                } else {
                    LblReshta.setText("Решта: " + (pay - p) + " грн");
                    BtnPie.setEnabled(true);
                }
            } catch (NumberFormatException e) {
                LblReshta.setText("Невірний формат");
                BtnPie.setEnabled(false);
            }
        }
    }//GEN-LAST:event_PayKeyPressed

