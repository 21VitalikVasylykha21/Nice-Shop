    private void JTreeShopMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JTreeShopMouseClicked
        TreePath Path = JTreeShop.getPathForLocation(evt.getX(), evt.getY());
        DefaultTableModel ModelJTableCounters = (DefaultTableModel) JTableCounters.getModel();
        ModelJTableCounters.setRowCount(0);
        if (Path != null) {
            PathToFile = "";
            for (int i = 0; i < Path.getPathCount(); i++) {
                PathToFile += Path.getPathComponent(i) + "\\";
            }
            if (evt.getButton() == MouseEvent.BUTTON1 & !PathToFile.equals("Магазини\\")) {
                ArrayList<String> textFiles = new ArrayList<>();
                textFiles = textFiles(PathToFile + "\\На прилавках\\", ".json");
                for (String s : textFiles) {
                    GDS = GDS.FromGson(GDS.ReadGsonFile(PathToFile + "\\На прилавках\\" + s), GDS);
                    ModelJTableCounters.addRow(new Object[]{GDS.GetName(), GDS.GetIndex(), GDS.GetQuantity(), GDS.GetPrice(), GDS.GetProducer()});
                }
            }
            if (evt.getButton() == MouseEvent.BUTTON3) {
                BtnAddGoodsIcon.setEnabled(true);
                BtnAddGoodsWithStorageIcon.setEnabled(true);
                BtnAddShopIcon.setEnabled(true);
                BtnRemoveShopIcon.setEnabled(true);
                BtnAddCashierIcon.setEnabled(true);
                BtnReportIcon.setEnabled(true);
            }
            jComBoxCasa.removeAllItems();
            textFiles(PathToFile, ".json").forEach(d -> {
                String[] tokens = d.split("\\.(?=[^\\.]+$)");
                jComBoxCasa.addItem(tokens[0]);
            });
        }
    }//GEN-LAST:event_JTreeShopMouseClicked

