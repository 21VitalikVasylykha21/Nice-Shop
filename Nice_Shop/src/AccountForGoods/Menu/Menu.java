    private ArrayList<String> textFiles(String directory, String f) {
        ArrayList<String> textFiles = new ArrayList<>();
        File dir = new File(directory);
        for (File file : dir.listFiles()) {
            if (file.getName().endsWith((f))) {
                textFiles.add(file.getName());
            }
        }

        return textFiles;
    }

    public void recursiveDelete(File file) {
        if (!file.exists()) {
            return;
        }
        if (file.isDirectory()) {
            for (File q : file.listFiles()) {
                recursiveDelete(q);
            }
        }
        file.delete();
    }

    public void Filter(String Find) {
        DefaultTableModel model = (DefaultTableModel) JTableCounters.getModel();
        TableRowSorter<DefaultTableModel> filter = new TableRowSorter<>(model);
        JTableCounters.setRowSorter(filter);
        filter.setRowFilter(RowFilter.regexFilter(Find));
    }

